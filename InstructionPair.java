public class InstructionPair {
	
	// Data members
	private String bot;
	private int button;
	
	// Constructor
	public InstructionPair(String bot, int button) {
		this.bot = bot;
		this.button = button;
	}
	
	// Getters
	public String getBot() { return bot; }
	public int getButton() { return button; }
 
}
